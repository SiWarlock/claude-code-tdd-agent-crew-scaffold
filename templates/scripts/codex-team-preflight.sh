#!/usr/bin/env bash
# ⚠ EXPERIMENTAL — Codex multi-agent team overlay (WIP). Built on Codex's unstable
# collaboration_mode / spawn_agent v2 APIs (expect churn; pin a known-good version). No native git-worktree
# isolation; `codex exec` exits 0 even on failure (verify via git log + test re-run, never the exit code);
# --output-schema was gpt-5-family only on some past Codex builds (openai/codex#4181, fixed by #4195) —
# confirm on your pinned version rather than assuming either way (coding roles report via a filesystem
# ledger regardless); OFF by default. Full caveats + design: docs/codex/team-overlay.md.

set -euo pipefail

# codex-team-preflight.sh — the /team-start churn-guard (generated by scaffold-generate; placeholder-only).
# Run FIRST by the Codex /team-start skill, BEFORE any spawn:  scripts/codex-team-preflight.sh "<label>"
#
# It is the ONE deterministic gate that proves spawn/result/close on the *installed* (unstable) Codex
# build. It does two things (design doc §6):
#   (a) assert the codex CLI exists and config.toml parses with the [agents.*] role blocks loaded;
#   (b) a TRIVIAL spawn_agent round-trip probe — spawn a child that writes a sentinel to
#       .codex-team/$TEAM_LABEL/probe.json and returns it; wait_agent with a timeout; close_agent; then
#       INDEPENDENTLY assert the sentinel is in probe.json (NEVER read success off an exit code — codex
#       exec exits 0 even on failure).
# On ANY failure it prints the documented solo-fallback line and exits non-zero; /team-start then stops
# and the session runs the supported solo core (/tdd directly).
#
# DURABLE vs BEST-EFFORT — the durable parts are the STRUCTURE and the solo fallback. The actual `codex`
# collab invocations are EXPERIMENTAL best-effort and CANNOT be runtime-tested here; they are clearly
# commented so you can adapt them to your pinned Codex build. By design the probe fails SAFE: with no real
# collab layer wired (or a broken one), probe.json is never written, the sentinel assertion fails, and the
# overlay falls back to solo — exactly the right default for an unstable, opt-in accelerant.
#
# CIRCUIT BREAKER — this script keeps NO circuit-breaker state of its own. The orchestrator's ledger
# (.codex-team/<label>/tasks.jsonl `circuit_open` row) is the sole source of truth (see
# .codex/agents/orchestrator.toml's "3-failure circuit breaker" section) — there is no separate counter
# file here for the orchestrator (an LLM agent operating via tool calls, not a bash-function caller) to
# call into. A fresh /team-start attempt implicitly means a human has already intervened past any prior
# `circuit_open` row; this preflight does not gate on ledger history from a previous run.

# label: positional arg from /team-start, else env TEAM_LABEL, else "team".
TEAM_LABEL="${1:-${TEAM_LABEL:-team}}"
# CODEX_PROJECT_DIR does not exist under any Codex build; resolve the repo root via git instead.
# The `|| true` matters: without it, a failing git rev-parse (e.g. cwd isn't inside a git worktree) would
# trip `set -e` on this bare command-substitution assignment and kill the script with a raw git error,
# before ever reaching the documented solo_fallback() path below.
PROJECT_DIR=$(git rev-parse --show-toplevel 2>/dev/null) || true
PROJECT_DIR="${PROJECT_DIR:-$PWD}"
TEAM_DIR="$PROJECT_DIR/.codex-team/$TEAM_LABEL"
PROBE="$TEAM_DIR/probe.json"
PROBE_TIMEOUT_MS="${CODEX_TEAM_PROBE_TIMEOUT_MS:-20000}"

mkdir -p "$TEAM_DIR" 2>/dev/null || true

# ── Solo fallback (durable) ───────────────────────────────────────────────────────────────────────
# Printed verbatim on ANY preflight failure. The generated AGENTS.md carries the complete solo path, so
# solo is always a working mode. Never "spawn anyway" — a failed preflight means the spawn tree is not
# trustworthy on this Codex build.
solo_fallback() {
  echo
  echo 'Codex collab layer unavailable — falling back to solo. Run `/tdd` directly in this session.'
  exit 1
}

# ── (a) config + role-block load check ────────────────────────────────────────────────────────────
# (a1) HARD requirement — the codex CLI must exist (it may be absent in CI / on a plain checkout).
command -v codex >/dev/null 2>&1 || { echo "codex CLI not found — cannot run team overlay; use solo /tdd"; exit 1; }

# (a2) BEST-EFFORT — prove `codex` starts and this repo's config.toml parses with [agents.*] loaded. The
# exact validator subcommand is version-dependent on the experimental collab build (it has churned), so
# only an explicit failure of a known validator is treated as fatal here; an unrecognized subcommand is
# inconclusive and deferred to the authoritative live spawn probe in (b). Adapt to your pinned build.
config_smoke() {
  codex --version >/dev/null 2>&1 || return 1          # the binary at least runs
  # Uncomment the validations your Codex build supports (names are version-dependent / experimental):
  #   codex config check   >/dev/null 2>&1 || return 1   # validates config.toml parses
  #   codex agents list    >/dev/null 2>&1 || return 1   # confirms the [agents.*] role blocks load
  return 0
}
if ! config_smoke; then
  echo "preflight: codex did not start cleanly (config.toml parse / [agents.*] role-load smoke failed)." >&2
  solo_fallback
fi

# ── (b) trivial spawn_agent round-trip probe ──────────────────────────────────────────────────────
# Generate a unique sentinel, attempt the spawn/wait/close round-trip (best-effort, commented), then
# assert the sentinel landed in probe.json. The assertion — NOT any exit code — is the gate.
SENTINEL="codex-probe-$$-$(date +%s 2>/dev/null || echo 0)"
rm -f "$PROBE" 2>/dev/null || true

# probe_roundtrip — EXPERIMENTAL best-effort. spawn_agent / wait_agent / close_agent are the unstable v2
# surface; their exact CLI/MCP shape WILL churn. We do NOT trust any exit code (codex exec exits 0 even
# on failure); the only thing that counts is whether the child wrote the sentinel to probe.json below.
# Returns non-zero only on a hard spawn ERROR; a clean-but-empty run is caught by the sentinel assertion.
probe_roundtrip() {
  # --- spawn_agent(role=orchestrator): child writes {"sentinel":"$SENTINEL"} to $PROBE and returns it ---
  # Example shape (adapt to your pinned Codex build; lines intentionally inert until wired):
  #   handle=$(codex agent spawn --role orchestrator --task /root/probe \
  #              --prompt "write {\"sentinel\":\"$SENTINEL\"} to $PROBE and return that JSON" 2>/dev/null) \
  #     || return 1
  #   # wait_agent(timeout_ms): a timeout is NOT trust — the sentinel check below is authoritative.
  #   codex agent wait  "$handle" --timeout-ms "$PROBE_TIMEOUT_MS" >/dev/null 2>&1 || true
  #   # close_agent: reclaim the child (best-effort; failure here is not fatal to the probe).
  #   codex agent close "$handle" >/dev/null 2>&1 || true
  #
  # Until the real collab calls above are wired for your build, probe.json is never written, so the
  # sentinel assertion fails and the overlay falls back to solo — the correct, safe default.
  return 0
}

if ! probe_roundtrip; then
  echo "preflight: spawn_agent round-trip errored (collab layer absent or spawn unavailable)." >&2
  solo_fallback
fi

# Authoritative assertion — the sentinel must be in probe.json (round-trip actually completed).
if [ ! -f "$PROBE" ] || ! grep -qF "$SENTINEL" "$PROBE" 2>/dev/null; then
  echo "preflight: probe sentinel not found in $PROBE within ${PROBE_TIMEOUT_MS}ms — spawn/result/close round-trip did not complete." >&2
  echo "          (Reminder: never read success off an exit code; codex exec exits 0 even on failure.)" >&2
  solo_fallback
fi

# ── Pass ──────────────────────────────────────────────────────────────────────────────────────────
rm -f "$PROBE" 2>/dev/null || true
echo "preflight OK: codex present, config smoke passed, spawn_agent round-trip verified (sentinel in probe.json)."
echo "             the orchestrator's ledger (.codex-team/$TEAM_LABEL/tasks.jsonl) is the circuit breaker's source of truth."
exit 0
