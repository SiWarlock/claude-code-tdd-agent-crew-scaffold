#!/usr/bin/env bash
# ⚠ EXPERIMENTAL — Codex multi-agent team overlay (WIP). Built on Codex's unstable
# collaboration_mode / spawn_agent v2 APIs (expect churn; pin a known-good version). No native git-worktree
# isolation; `codex exec` exits 0 even on failure (verify via git log + test re-run, never the exit code);
# --output-schema was gpt-5-family only on some past Codex builds (openai/codex#4181, fixed by #4195) —
# confirm on your pinned version rather than assuming either way (coding roles report via a filesystem
# ledger regardless); OFF by default.
# Full caveats + design: docs/codex/team-overlay.md.

set -euo pipefail

# subagent-start.sh — Codex SubagentStart hook (generated by scaffold-generate; placeholder-only).
# Wired in config.toml under [[hooks.SubagentStart]] for host=codex + mode=team (see
# templates/config.codex.team.toml). Replaces the Claude team-event-log.sh's TeammateIdle branch.
#
# WHAT IT DOES — appends ONE durable JSON line {ts,event:"start",agent,task} to
#   .codex-team/$TEAM_LABEL/events.jsonl
# so the lead/orchestrator can reconstruct the spawn timeline (which roles were forked, against which
# canonical task paths) beyond Codex's ephemeral in-process signals. Paired with subagent-stop.sh,
# the events trail is also how "spawned but no ledger row" drift is noticed (design doc §6).
#
# WARN-ONLY / NEVER-FAIL — this is an observability hook. Every fallible step is guarded; the script
# always exits 0. A Codex hook that hard-fails would block the spawn it is only meant to observe.
#
# Field names: `agent_id`/`agent_type` are the confirmed real SubagentStart payload fields (per Codex's
# hooks docs). There is no documented `task`/`task_path`-shaped field on this payload at all — those guesses
# are kept only as best-effort fallbacks in case a future/pinned build adds one; do not rely on them.
# CODEX_PROJECT_DIR does not exist under any Codex build — the payload's own `cwd` field (real, documented)
# is used instead, with `git rev-parse --show-toplevel` as a subdirectory-safe fallback.

TEAM_LABEL="${TEAM_LABEL:-team}"             # ledger/event-log dir key (single-track v1: no <track> prefix)

# Read the hook payload off stdin if present (a real hook gets JSON, not a tty).
payload=""
if [ ! -t 0 ]; then
  payload=$(cat 2>/dev/null || true)
fi

# get <jq-path> — best-effort extraction from the payload; always returns 0 (never trips set -e).
get() {
  command -v jq >/dev/null 2>&1 || return 0
  [ -n "$payload" ] || return 0
  printf '%s' "$payload" | jq -r "$1 // empty" 2>/dev/null || return 0
}

PROJECT_DIR=$(get '.cwd')
[ -n "$PROJECT_DIR" ] || PROJECT_DIR=$(git rev-parse --show-toplevel 2>/dev/null) || true
PROJECT_DIR="${PROJECT_DIR:-$PWD}"
TEAM_DIR="$PROJECT_DIR/.codex-team/$TEAM_LABEL"
EVENTS="$TEAM_DIR/events.jsonl"

# agent_id/agent_type are the real, documented fields; the rest are legacy best-effort guesses kept as a
# fallback chain in case an older/different pinned build shapes this payload differently.
agent=$(get '.agent_type // .agent_id // .agent // .role // .subagent_type // .agent_role')
[ -n "$agent" ] || agent="${CODEX_AGENT_ROLE:-${CODEX_SUBAGENT:-unknown}}"
task=$(get '.task // .task_path // .task_id // .thread')
[ -n "$task" ] || task="${CODEX_TASK_PATH:-${CODEX_TASK_ID:-}}"

ts=$(date -u +%Y-%m-%dT%H:%M:%SZ 2>/dev/null || echo unknown)
mkdir -p "$TEAM_DIR" 2>/dev/null || true

# emit <event> [reason] — append one JSON line to events.jsonl. Prefers jq; falls back to a
# hand-rolled, escaped JSON line so the log still records when jq is absent.
emit() {
  local ev="${1:-unknown}" reason="${2:-}"
  if command -v jq >/dev/null 2>&1; then
    jq -nc \
      --arg ts "$ts" --arg event "$ev" --arg agent "$agent" --arg task "$task" --arg reason "$reason" \
      '{ts:$ts, event:$event, agent:$agent, task:$task}
        + (if $reason != "" then {reason:$reason} else {} end)' \
      >> "$EVENTS" 2>/dev/null || true
  else
    local a="$agent" t="$task" r="$reason" extra=""
    a=${a//\\/\\\\}; a=${a//\"/\\\"}
    t=${t//\\/\\\\}; t=${t//\"/\\\"}
    r=${r//\\/\\\\}; r=${r//\"/\\\"}
    if [ -n "$r" ]; then extra=",\"reason\":\"$r\""; fi
    printf '{"ts":"%s","event":"%s","agent":"%s","task":"%s"%s}\n' "$ts" "$ev" "$a" "$t" "$extra" \
      >> "$EVENTS" 2>/dev/null || true
  fi
}

emit start

# ── Multi-track worktree provisioning (PRESENT-BUT-DORMANT — single-track v1) ──────────────────────
# v1 ships single-track only: ONE spawn-tree in the repo's working tree, no per-track worktree. Multi-
# track (N parallel spawn-trees, each isolated in its own git worktree) is DEFERRED (design doc §2) —
# N spawn-trees over an unstable collab layer with NO native worktree isolation is too much risk surface,
# and out-of-repo worktrees are not stash-protected. This is the dormant hook POINT, kept so the wiring
# is visible; it runs ONLY when CODEX_TEAM_MULTITRACK=1 is explicitly set. Default OFF. Even when on,
# any failure here is swallowed (best-effort) — provisioning must never fail the spawn it observes.
if [ "${CODEX_TEAM_MULTITRACK:-0}" = "1" ]; then
  track="$TEAM_LABEL"                                   # in multi-track, TEAM_LABEL doubles as the track name
  repo_dirname=$(basename "$PROJECT_DIR" 2>/dev/null || echo repo)
  worktree_path="$PROJECT_DIR/../${repo_dirname}-${track}"
  branch="track/${track}"
  # Idempotent: only provision if the worktree path does not already exist (per-track, not per-spawn).
  if command -v git >/dev/null 2>&1 && [ ! -e "$worktree_path" ]; then
    git -C "$PROJECT_DIR" show-ref --verify --quiet "refs/heads/${branch}" \
      || git -C "$PROJECT_DIR" branch "$branch" 2>/dev/null || true
    git -C "$PROJECT_DIR" worktree add "$worktree_path" "$branch" 2>/dev/null || true
    emit worktree "provisioned ${worktree_path} on ${branch}"
  fi
fi

exit 0
